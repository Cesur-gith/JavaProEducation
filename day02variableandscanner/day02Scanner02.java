package day02variableandscanner;

import java.util.Scanner;

public class day02Scanner02 {

	public static void main(String[] args) {

		/*
		 * Get 2 number from user and print the sume of the console
		 */
		// Create Scanner object and import Scanner Class
		Scanner scan = new Scanner(System.in);
		System.out.println("Enter first number...");
		int number1 = scan.nextInt(); // Get the data and put into the container 
		
		System.out.println("Enter second number...");
		int number2 = scan.nextInt();
		
		System.out.println("The sum: " + (number1 + number2));

				
		
		
		
		
		
		
		
		
		
		
		
	}

}
